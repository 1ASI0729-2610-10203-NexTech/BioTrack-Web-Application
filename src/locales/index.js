export const defaultLocale = 'en-US'
export const fallbackLocale = 'en-US'
export const localeStorageKey = 'biotrack.locale'

export function normalizeLocale(locale) {
  const normalized = String(locale ?? '').replace('_', '-')
  if (normalized.toLowerCase().startsWith('es')) return 'es-419'
  return 'en-US'
}

export function resolveInitialLocale() {
  const storedLocale = localStorage.getItem(localeStorageKey)
  if (storedLocale) return normalizeLocale(storedLocale)
  return defaultLocale
}

function resolveMessage(key, locale) {
  return key.split('.').reduce((current, part) => current?.[part], messages[locale])
}

function interpolateMessage(message, params) {
  return String(message).replace(/\{(\w+)\}/g, (_, param) => params[param] ?? '')
}

export function t(key, params = {}, locale = resolveInitialLocale()) {
  const normalizedLocale = normalizeLocale(locale)
  const message =
    resolveMessage(key, normalizedLocale) ??
    resolveMessage(key, fallbackLocale) ??
    key
  return interpolateMessage(message, params)
}

export function tm(key, locale = resolveInitialLocale()) {
  const normalizedLocale = normalizeLocale(locale)
  return resolveMessage(key, normalizedLocale) ?? resolveMessage(key, fallbackLocale)
}

